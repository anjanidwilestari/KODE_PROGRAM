    public void tampilkanData() {
        String[] kolom = {"Id_obat", "kode_obat", "nama_obat", "kategori_obat", "jenis_obat", "merek_obat", "harga_beli", "harga_jual", "jumlah_obat", "tanggal_masuk", "expired"};
        ArrayList<obat> list = new obat().getAll();
        Object rowData[] = new Object[11];
        
        tableobat.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for (int i = 0; i < list.size(); i++){
            rowData[0] = list.get(i).getId_obat();
            rowData[1] = list.get(i).getKode_obat();
            rowData[2] = list.get(i).getNama_obat();
            rowData[3] = list.get(i).getKategori().getNama();
            rowData[4] = list.get(i).getJenis_obat();
            rowData[5] = list.get(i).getMerek_obat();
            rowData[6] = list.get(i).getHarga_beli_obat();
            rowData[7] = list.get(i).getHarga_jual_obat();
            rowData[8] = list.get(i).getJumlah_obat();
            rowData[9] = list.get(i).getTanggal_masuk();
            rowData[10] = list.get(i).getExpired();
            
            ((DefaultTableModel)tableobat.getModel()).addRow(rowData);
        }
    }
    
