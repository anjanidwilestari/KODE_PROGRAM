    private void tableobatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableobatMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tableobat.getModel();
        int row = tableobat.getSelectedRow();
        obat ob = new obat();
        
        ob = obat.getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        
        txtidobat.setText(Integer.toString(ob.getId_obat()));
        txtkodeobat.setText(ob.getKode_obat());
        txtnama.setText(ob.getNama_obat());
        cmbkategori.getModel().setSelectedItem(ob.getKategori());
        txtjenis.setText(ob.getJenis_obat());
        txtmerek.setText(ob.getMerek_obat());
        txthargabeli.setText(Integer.toString(ob.getHarga_beli_obat()));
        txthargajual.setText(Integer.toString(ob.getHarga_jual_obat()));
        txtjumlahstok.setText(Integer.toString(ob.getJumlah_obat()));
        txttglmasuk.setText(ob.getTanggal_masuk());
        txtexpired.setText(ob.getExpired());
    }//GEN-LAST:event_tableobatMouseClicked

